    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
try {
            String sql = "INSERT INTO tbl_customer (id_customer, nama_customer, no_telp, alamat) VALUES (?, ?, ?, ?)";
            java.sql.Connection open_koneksi = KoneksiPenjualan.configDB();
            java.sql.PreparedStatement pst = open_koneksi.prepareStatement(sql);
            
            pst.setString(1, txtId.getText());
            pst.setString(2, txtNama.getText());
            pst.setString(3, txtTelp.getText());
            pst.setString(4, txtAlamat.getText());
            
            pst.execute();
            javax.swing.JOptionPane.showMessageDialog(null, "Penyimpanan Data Berhasil");
            
            load_table();
            kosong();
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal simpan data: " + e.getMessage());
        }        // TODO add your handling code here:
    }//GEN-LAST:event_btnSimpanActionPerformed

