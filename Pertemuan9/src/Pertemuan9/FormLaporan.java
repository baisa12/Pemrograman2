    private void btnLapBarangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLapBarangActionPerformed
DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Kode Barang");
        model.addColumn("Nama Barang");
        model.addColumn("Harga");
        model.addColumn("Stok");

        try {
            String sql = "SELECT * FROM tbl_barang";
            Connection open_koneksi = KoneksiPenjualan.configDB();
            Statement stat = open_koneksi.createStatement();
            ResultSet res = stat.executeQuery(sql);
            while (res.next()) {
                model.addRow(new Object[]{res.getString(1), res.getString(2), res.getString(3), res.getString(4)});
            }
            tblLaporan.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data: " + e.getMessage());
        }        // TODO add your handling code here:
    }//GEN-LAST:event_btnLapBarangActionPerformed

