    private void txtIdCustActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtIdCustActionPerformed
try {
            String sql = "SELECT * FROM tbl_customer WHERE id_customer = '" + txtIdCust.getText() + "'";
            Connection open_koneksi = KoneksiPenjualan.configDB();
            Statement stat = open_koneksi.createStatement();
            ResultSet res = stat.executeQuery(sql);

            if (res.next()) {
                // Jika ID ditemukan, isi nama dan pindahkan kursor ke Kode Barang
                txtNamaCust.setText(res.getString("nama_customer"));
                txtKodeBrg.requestFocus(); 
            } else {
                // Jika ID tidak ada di database
                JOptionPane.showMessageDialog(null, "Customer Tidak Ditemukan!");
                txtNamaCust.setText("");
                txtIdCust.setText("");
                txtIdCust.requestFocus();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }        // TODO add your handling code here:
    }//GEN-LAST:event_txtIdCustActionPerformed

