    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
try {
            java.sql.Connection open_koneksi = KoneksiPenjualan.configDB();
            
            // 1. Simpan data utama ke tbl_transaksi
            String sql_master = "INSERT INTO tbl_transaksi (no_faktur, tgl_transaksi, id_customer, total_bayar) VALUES (?, ?, ?, ?)";
            java.sql.PreparedStatement pst_master = open_koneksi.prepareStatement(sql_master);
            pst_master.setString(1, txtFaktur.getText());
            pst_master.setString(2, txtTanggal.getText()); // Wajib format YYYY-MM-DD
            pst_master.setString(3, txtIdCust.getText());
            pst_master.setString(4, txtTotal.getText());
            pst_master.execute();
            
            // 2. Simpan daftar belanjaan ke tbl_transaksi_detail (Pake Looping dari Tabel Keranjang)
            int baris = tblDetail.getRowCount();
            for (int i = 0; i < baris; i++) {
                String sql_detail = "INSERT INTO tbl_transaksi_detail (no_faktur, kode_barang, harga, jumlah, subtotal) VALUES (?, ?, ?, ?, ?)";
                java.sql.PreparedStatement pst_detail = open_koneksi.prepareStatement(sql_detail);
                pst_detail.setString(1, txtFaktur.getText());
                pst_detail.setString(2, tblDetail.getValueAt(i, 0).toString());
                pst_detail.setString(3, tblDetail.getValueAt(i, 2).toString());
                pst_detail.setString(4, tblDetail.getValueAt(i, 3).toString());
                pst_detail.setString(5, tblDetail.getValueAt(i, 4).toString());
                pst_detail.execute();
            }
            
            javax.swing.JOptionPane.showMessageDialog(null, "Transaksi Berhasil Disimpan ke Database!");
            
            // Otomatis klik tombol Batal untuk membersihkan layar setelah sukses
            btnBatal.doClick(); 
            
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal Simpan Transaksi: " + e.getMessage());
        }        // TODO add your handling code here:
    }//GEN-LAST:event_btnSimpanActionPerformed

