    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
try {
            String kode = txtKodeBrg.getText();
            String nama = txtNamaBrg.getText();
            int harga = Integer.parseInt(txtHarga.getText());
            int jumlah = Integer.parseInt(txtJumlah.getText());
            int subtotal = harga * jumlah; // Menghitung otomatis Harga x Jumlah

            // Memasukkan data ke dalam tabel keranjang
            keranjang.addRow(new Object[]{kode, nama, harga, jumlah, subtotal});
            
            // Panggil fungsi hitung total
            hitung_total();

            // Kosongkan form input barang agar siap untuk barang berikutnya
            txtKodeBrg.setText("");
            txtNamaBrg.setText("");
            txtHarga.setText("");
            txtJumlah.setText("");
            txtKodeBrg.requestFocus();
            
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Pastikan Jumlah Beli diisi dengan angka!");
        }        // TODO add your handling code here:
    }//GEN-LAST:event_btnTambahActionPerformed

